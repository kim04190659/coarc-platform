// ── ヘルパー関数 ─────────────────────────────────────

/** gameId からクイズデータを取得 */
export function getQuizForGame(gameId: string): GameQuiz | undefined {
  return GAME_QUIZZES.find(q => q.gameId === gameId)
}

/** gameId からクイズ問題リストを取得 */
export function getQuestionsForGame(gameId: string): QuizQuestion[] {
  return getQuizForGame(gameId)?.questions ?? []
}
