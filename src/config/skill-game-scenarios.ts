/** gameId に対応するシナリオリストを返す */
export function getScenariosForGame(gameId: string): Scenario[] {
  return GAME_SCENARIOS.find(g => g.gameId === gameId)?.scenarios ?? []
}

/** シナリオIDからシナリオを取得 */
export function getScenarioById(gameId: string, scenarioId: string): Scenario | undefined {
  return getScenariosForGame(gameId).find(s => s.id === scenarioId)
}
